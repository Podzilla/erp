package com.Podzilla.analytics.eventhandler.DTOs;

/**
 * Data Transfer Object (DTO) for example purposes.
 *
 * This class represents a sample DTO. It is recommended to replace it with a
 * meaningful class in your actual code.
 */
public class ADTO {

    /** Represents the 'a' property in the DTO. */
    private String a;

    /**
     * Gets the value of 'a'.
     *
     * @return the value of 'a'
     */
    public String getA() {
        return a;
    }

    /**
     * Sets the value of 'a'.
     *
     * @param value the value to set
     */
    public void setA(final String value) {
        this.a = value;
    }
}
